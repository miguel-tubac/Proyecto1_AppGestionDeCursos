    private void CrearBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearBtn1ActionPerformed
        // TODO add your handling code here:
        String codigo = CodigoInput.getText();
        String nombre = NombreInput.getText();
        String apellido = ApellidoInput.getText();
        String correo = CorreoInput.getText();
        String contrasenia = contrasena.getText();
        String genero = (String) gener.getSelectedItem();
        
        if(codigo.isEmpty() || nombre.isEmpty()|| apellido.isEmpty()||correo.isEmpty() ||contrasenia.isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"INGRESE TODOS LOS CAMPOS");
        }else{
            Profesor nuevoProfesor = new Profesor(codigo, nombre, apellido, correo, contrasenia, genero);
        int contadorProfesores = AdministradorProfesores.contadorProfesores;
        
        AdministradorProfesores.profesores[contadorProfesores] = nuevoProfesor;
        
        AdministradorProfesores.contadorProfesores++;
        CodigoInput.setText("");
        NombreInput.setText("");
        ApellidoInput.setText("");
        CorreoInput.setText("");
        contrasena.setText("");
        JOptionPane.showMessageDialog(rootPane,"PROFESOR AGREGADO CON EXITO");
        }
        
    }//GEN-LAST:event_CrearBtn1ActionPerformed

