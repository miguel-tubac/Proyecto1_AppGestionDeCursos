    private void ActualizarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarBtnActionPerformed
        // TODO add your handling code here:
        String codigo = CodigoInput.getText();
        String nombre = NombreInput.getText();
        String apellido = ApellidoInput.getText();
        String correo = CorreoInput.getText();
        String contrasenia = PasswordInput.getText();
        String genero = (String) gener.getSelectedItem();
        
        if(codigo.isEmpty() || nombre.isEmpty()|| apellido.isEmpty()||correo.isEmpty() ||contrasenia.isEmpty()){
            JOptionPane.showMessageDialog(rootPane,"INGRESE TODOS LOS CAMPOS");
            meterDatos();
        }else{
            Profesor nuevoProfesor = new Profesor(codigo, nombre, apellido, correo, contrasenia, genero);
            int fila = AdministradorProfesores.filaP;
            AdministradorProfesores.profesores[fila] = nuevoProfesor;
            
            JOptionPane.showMessageDialog(rootPane,"PROFESOR ACTUALIZADO CON EXITO");
            AdministradorProfesores objet=new AdministradorProfesores();
            objet.show(true);
            this.show(false);
        }

       


    }//GEN-LAST:event_ActualizarBtnActionPerformed

    public void meterDatos() {
        CodigoInput.setText(AdministradorProfesores.codigo);
        NombreInput.setText(AdministradorProfesores.nombre);
        ApellidoInput.setText(AdministradorProfesores.apellido);
        CorreoInput.setText(AdministradorProfesores.correo);
        PasswordInput.setText(AdministradorProfesores.contrasenia);
        //gener.setText(AdministradorProfesores.genero);
        int indice;
        if(AdministradorProfesores.genero.equals("m")){
            indice=0;
        }else{
           indice=1; 
        }
        gener.setSelectedIndex(indice);
    }

