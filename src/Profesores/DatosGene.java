public class DatosGene {

    public String codigo;
    public String nombre;
    public String apellido;
    public String correo;
    public double Nota;

    public DatosGene(String codigo, String nombre, String apellido,
            String correo, double Nota) {
        this.codigo = codigo;
        this.nombre = nombre;
        this.apellido = apellido;
        this.correo = correo;
        this.Nota = Nota;
        
    }

}
